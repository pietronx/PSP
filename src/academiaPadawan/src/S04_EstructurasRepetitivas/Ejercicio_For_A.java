package S04_EstructurasRepetitivas;

/*
Crea un programa que muestre por pantalla todos los números entre 5 y 150
 */
public class Ejercicio_For_A {
    public static void main(String[] args) {
        int i;

        for (i = 5; i <= 150; i++) {
            System.out.println("Número: " + i);
        }
    }
}
