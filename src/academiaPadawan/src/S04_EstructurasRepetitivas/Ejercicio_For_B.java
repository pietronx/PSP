package S04_EstructurasRepetitivas;

/*
Crea un programa que pida un nombre y luego muestre 100 veces por pantalla "Hola soy [tuNombre]"
 */
public class Ejercicio_For_B {

    public static void main(String[] args) {
        int i;

        for (i = 1; i <= 100; i++) {
            System.out.println("Hola, soy Pedro: " + i);
        }
    }
}
